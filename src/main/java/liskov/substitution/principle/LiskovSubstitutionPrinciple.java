package liskov.substitution.principle;

public interface LiskovSubstitutionPrinciple
{
    // Derived classes must be substitutable for their base classes
    // If for each o1 object of type S, there is an o2 object of type T so that,
    // for all P programs, the behavior of P is unchanged when o1 is replaced by o2,
    // then S is a subtype of T
}
